#include"libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	a;

	i = 0;
	a = (char)c;
	while ((s[i] != '\0' || a == '\0'))
	{
		if (s[i] == a)
			return ((char *)(s + i));
		i++;
	}
	return (NULL);
}
